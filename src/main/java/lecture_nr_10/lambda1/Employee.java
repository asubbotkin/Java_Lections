package lecture_nr_10.lambda1;

public class Employee {
    String name;
    int age;
    boolean canConductTraining;
    boolean canConductInterview;

    public Employee(String name, int age, boolean canConductTraining, boolean canConductInterview) {
        this.name = name;
        this.age = age;
        this.canConductTraining = canConductTraining;
        this.canConductInterview = canConductInterview;
    }
}
